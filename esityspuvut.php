            <h2>Puvustuksen toteutus</h2>
            <p>Etenkin pienissä teatteri-, tanssi- ja oopperaryhmissä pukusuunnittelija saattaa osallistua pukujen toteutukseen varsin paljon tai jopa tehdä itse koko toteutustyön.
               Pukuja ei aina valmisteta alusta lähtien, vaan usein esityksen puvustus syntyy yhdistelemällä erilaisia elementtejä: vanhoja varastopukuja, jotain lainattua, jotain esitystä varten ostettua ja jotain sitä varten valmistettua.
