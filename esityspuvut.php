            <p>Puvuilla on oma kielensä, joka pitää tuntea silloin kun valmistetaan esitystä.
               Pukusuunnittelijan ammattitaitoon kuuluu ottaa huomioon monenlaiset seikat,
               joiden avulla puvuista rakentuu osa esityksen taiteellista kerrontaa; minkälaista tunnelmaa tavoitellaan,
                mitkä väreihin, materiaaleihin  tyyliin ja muotoon liittyvät ratkaisut parhaiten palvelevat kerrontaa ja esiintyjäntyötä,
