                 Lisäksi pukusuunnittelija huolehtii myös käytännön toteutukseen liittyvistä asioista,
                 esimerkiksi kuka valmistaa puvut, mistä materiaalit hankitaan,  miten pukuja huolletaan esityskauden aikana ja kuinka suuri budjetti on käytössä.
Pukusuunnittelijan lähimpiä yhteistyökumppaneita ovat esityksen ohjaaja ja koreografi, esiintyjät, lavastaja, valo- ja äänisuunnittelijat sekä tietysti pukujen toteuttajat.


