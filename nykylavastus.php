            <h2>Lavastus kerronnan osana</h2>
            <p>Esimerkiksi lavastaja Johannes Schütz’in lavastukset ovat pitkälle ajateltuja, yksinkertaisia, minimalistisia, esteettisiä ja samalla arkkitehtonisia mutta erittäin vaativia tilallisia ratkaisuja esityksen näyttelijöille.
               Ne luottavat katsojan mielikuvituskykyyn ja näyttelijöiden energisyyteen.
               Hänen lavastuksissaan ei ole naturalistisia ’kahvikuppeja’ tai muita realistisia ’keittiöelementtejä’, joskus niissä ei ole edes tarpeeksi tilaa näytellä. Lavastus toimii koko kerronnan osana. </p>
