          <h2>Uudet materiaalit</h2>
        <p>Lavastus käsitettiin näyttämöllistä toimintaa mahdollistavana maailmana, ei näytelmätekstin kuvituksena. Kuvitteellisen ja illusorisen ’huonekulissilavastuksen’ tilalle tuli materia – raaka puu, joka täytti koko näyttämön. Näyttämötilaan luotiin arkkitehtoninen lavastus, joka rikkoi näyttämörampin ja kantoi katsomoon asti. Näyttelijät eivät näytelleet toisilleen kaukana erillisessä näyttämökuvassa – vaan ratkaisu pakotti näyttelijät kontaktiin yleisön kanssa. Haluttiin tehdä todellista teatteria ja elettiin Brechtiläisen teatterin vaikutuksen aikaa.
            </p>
            <h2>Esityksiä teatteritalojen ulkopuolella</h2>
            <p>Holmberg toteutti esityksiä myös toisissa tiloissa kuin teatterinäyttämöillä kuten esimerkiksi
               Helsingin Kaupunginteatterin kellaribunkkerissa näytelmän Rosvot (1970) ja
               Suvilahden voimalassa näytelmän Kullervo(1981) lavastajana Måns Hedström sekä museon remontin alla Ateneumissa,
                Kepillä on kaksi päätä (1983) lavastajana Ralf Forsström.
              Näiden esitysten visuaalisuuteen vaikutti suuresti itse tilan arkkitehtuuri ja karaktääri sekä sen tuomat esitykselliset mahdollisuudet.
               Rohkeana ohjaajana Holmberg mahdollisti lavastajien rohkeaa visuaalista ajattelua.</p>
