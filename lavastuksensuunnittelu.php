            <h2>Tuotannolliset mahdollisuudet</h2>
            <p>Myös lavastuksen teknisesti toteuttavan työryhmän koko,
               teatterin tuotannolliset resurssit ja käytettävissä oleva budjetti vaikuttavat suuresti lavastajan ajatusten suuntautumiseen.
               Lavastajan tehtäviin kuuluu usein myös lavastusten pystytys- ja purkutapahtumien miettiminen,
                koska Suomessa laitosteatterit yleensä toimivat repertuaariteatteriperiaatteella.
                 Teattereissa esitetään monia näytelmiä samanaikaisesti.

Taiteelliset tavoitteet
Koko taiteellinen työryhmä (kirjailija, dramaturgi, ohjaaja, koreografi, lavastaja, pukusuunnittelija, valosuunnittelija, videosuunnittelija, projisointisuunnittelija, säveltäjä ja äänisuunnittelija)
 on merkittävä taiteellisia tavoitteita määriteltäessä.
Näyttelijät eivät yleensä osallistu ennakkosuunnitteluun.
Eri ammattiryhmien painotukset kuitenkin hieman vaihtelevat tuotannosta riippuen.
Useimmiten ajatusten vaihto työryhmän kesken alkaa luetun tekstin pohjalta.
 Kukin pohtii tekstin herättämiä ajatuksia yksilöllisesti ja yhteistyöryhmässä haetaan kosketuspintaa ohjaajan ajatuksille siitä, miksi juuri tällä hetkellä on valittu toteutettavaksi tämä näytelmä tai aihepiiri.
Taiteellinen lähtökohta muotoutuu pikkuhiljaa ja inspiraation lähdemateriaalit kootaan yhteen.
Ryhmän yhteistyö kypsyy ja kasvaa omakseen.
Esitys on myös aina siihen osallistuvan ryhmän taiteellis-filosofinen kannanotto tämän hetken elämään, maailmantilanteeseen – näytelmätekstistä esille nouseviin asioihin.
 Taide on tekijöilleen henkilökohtaista, taiteilija ’luovuttaa’ omiin teoksiinsa hyvin henkilökohtaisen panoksen..</p>
