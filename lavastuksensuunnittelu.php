        <h2>Suunnittelu on ajattelua</h2>
        <p>Lavastuksen suunnittelutyö on aina ajatustyötä.
          Luovat työskentelytavat teatterissa ja muussa esittävässä taiteessa ovat tietenkin
          jokaiselle taiteilijalle omanlaisensa sekä teatterista tai tekijäryhmästä riippuvaiset.</p>
<h2>Yhdessä tekeminen</h2>
<p>Esitystaide on kuitenkin aina muodoltaan yhteistyötaidetta sekä yksilötaidetta.
Lavastajan työ on taiteellista suunnittelutyötä, taiteellisen sisällön ja muodon
 tuottamista kulloisenkin esityksen käyttötarkoituksiin ja toisaalta samalla hyvin käytännönläheistä tuotannon,
 tekniikan ja eri ammattiryhmien työn ymmärtämistä.
</p>
  <br /><br />



        </p>
