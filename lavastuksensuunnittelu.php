            <h2>Lavastuksen toteutus</h2>
            <p>Lavastajan vastuulla on lavastuksen toteutuksen valvonta suunnitelmien pohjalta.
              Lavastaja selventää ajatuksellisia yksityiskohtia ja organisoi sekä delegoi työn eteenpäin lavastamon esimiehen kautta toteuttajille.
               Yhteistyö lavastuksen ja esityksen rekvisiitan toteuttajien kanssa on usein erittäin vuorovaikutteista.
Harjoitusten alettua lavastajan työ jakautuu ensi-iltaan asti kahtaalle: työnjohtotehtäviin verstaalla ja esityksen harjoitustilanteeseen.
 Harjoitusten siirryttyä harjoitushuoneesta näyttämölle myös suhde näyttämötekniikan työntekijöihin ja näyttämötekniikan toteutukseen on kiinteä.
Harjoitusaikana suhde ohjaajaan hieman muuttuu, koska ohjaajan keskiössä ovat nyt näyttelijät ja kokonaisuuden sisällöllinen toimivuus, henkilöohjaus ja näyttämön toiminta.
 Usein lavastaja ja ohjaaja kuitenkin pohtivat näyttämön ohjaukseen liittyviä toiminnallisia asioita harjoitustilanteessa ja harjoitusten ulkopuolellakin.
</p>
