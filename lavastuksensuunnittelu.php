        </p>
        <div class="column-100 inlineBlock">
          <h2>Esityksen maailma</h2>
        <p>Lavastaja vaikuttaa työssään esiintyjien ja esityksen katsojien väliseen tilakokemukseen sekä esityksessä elettävään maailmaan
           ja sen ajalliseen tyylilajiin sekä suuresti ohjaajan ohjauksellisiin ratkaisuihin.
          Usein puhutaan ohjaajan kädenjäljestä, ohjaajan visuaalisesta teatterikielestä
        – mutta todellisuudessa näitäkin esityksiä on ollut toteuttamassa lavastaja – ohjaajan partnerina.
           Toisin kun yleisesti tunnutaan edelleenkin ajattelevan, esityksen visuaalisen maailman ratkaisija ei ole ohjaaja, vaan useimmiten lavastaja.
           Lavastaja on ohjaajaa haastava vastapeluri ja toimii erittäin usein koko esityksen ’visuaalisena ohjaajana’.  </p>

        </div>
        <br /><br /><br /><br />
        <div class="column-100 inlineBlock">
            <h2>Ohjaajan ja lavastajan välinen vuoropuhelu</h2>
            <p>Ohjaaja on kuitenkin lavastajan tärkein yhteistyökumppani.
               Ohjaaja mahdollistaa omalla taiteellisella rohkeudellaan esityksen visuaalisen rohkeuden.
               Valitun näytelmän ja tulevan esityksen aihe ja sen tavoitteet avautuvat syvemmin ohjaajan kanssa käydyissä keskusteluissa.
               Mitä avoimempia ovat ohjaajan ja lavastajan suunnittelupalaverit, sitä vapaampi on yleensä henkilökohtainen ajatuksenlento ja siitä alkava luova prosessi.
               Monet tulevaan esitykseen liittyvät ideat syntyvät yhteistyössä.

            </p>
            <h2>Suunnittelun lähtökohtia</h2>
                <p>Lavastaja aloittaa oman työnsä yleensä niin, että hän lukee omassa rauhassaan valmiin näytelmätekstin,
                  tekstisynopsiksen, romaanin tai pohtii annettuun aihioon liittyviä mielleyhtymiään.
                  Tehtävän työn sisällön ja aiheen ymmärtäminen alkaa, kun jokin näytelmätekstissä puhuttelee lavastajaa henkilökohtaisesti.
                  Se voi olla mikä tahansa oivallus ja yksityiskohta, josta ajatus lähtee liikkeelle.
                   Mielleyhtymät voivat liittyä väreihin, omiin muistoihin, materiaaleihin tai vaikka ääniin ja valoihin.
Lavastaja etsii yleensä alusta lähtien kuvallisen virikemateriaalin kautta omien ajatustensa havainnollistamista tai piirtää ajatuksistaan luonnoksia.
  Hän suhteuttaa ajatuksiaan alusta lähtien myös esitystilaan ja tutustuu siihen uudestaan
   ja uudestaan juuri tätä uutta tuotantoa silmällä pitäen. Esitystila, ja sen suhde esitettävän tekstin aiheeseen,
    määrittää lavastukselliset suuntaviivat ja ohjauksellisen tyylin.
</p>
