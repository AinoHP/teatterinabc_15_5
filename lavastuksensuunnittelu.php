            <h2>Suunnittelun aikataulu</h2>
            <p>Suunnittelutyö voi alkaa vuottakin aikaisemmin ennen varsinaisten harjoitusten alkua.
               Lavastajan luonnokset, esitystilan ja lavastuksen pienoismalli, pohjakuvat ja tekniset rakennepiirustukset toimivat työkaluina ja ratkaisumalleina kohti tulevaa toteutusta.
               Työskentelyaikataulun tärkeä päämäärä lavastajan ja ohjaajan kannalta on yleensä mallipalaveri, joka pidetään hyvissä ajoin teatterissa ennen harjoitusten alkua. Teatteri haluaa silloin valmiit suunnitelmat toteuttamisen aloittamista varten. Siihen mennessä taiteellisesti merkittävät päätökset tulee olla tehtynä.
                Vaikka esityksen toteutus olisikin prosessiluonteinen ja muotoutuisi paljolti myös harjoitusten myötä, teatteri usein haluaa selventää työpajoissa tehtävän työn määrän, laadun ja suuruuden. Lavastaja esittää rakentamansa pienoismallin, 3D-kuvan tai muita luonnoksia,
                joista tehtävän työn määrä selkiytyy lavastuksen toteuttajille.
                 Tässä vaiheessa lavastajalla itsellään tulee olla selkeä suhde tilaan ja näytelmän tilalliseen toteutukseen,
                  jotta toiminnallisesti tärkeät elementit saadaan valmiiksi rakennettua tai mahdollisesti hankittua harjoitusten alkuun mennessä.
                   Päätetyt tilaratkaisut ovat myös ohjauksellisia ratkaisuja.
 </p>
