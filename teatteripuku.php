          <h2>Vaate apuna muuttumisessa</h2>
            <p>Länsimaisen teatterin katsotaan saaneen alkunsa noin 500-vuosiadalla eaa. vietetyistä Dionysos -juhlista.
                Seremonioihini, joita aluksi vietettiin papin johdolla, tuli mukaan kuoro joka lauloi ja tanssi.
                Kuorolaiset olivat pukeutuneet eläimiksi, mainadeiksi tai satyyreiksi.

                Uskonnollisten riittien osallistuja luultavasti uskoivat, että eläimeksi pukeutunut kuorolainen muuttui
                oikeasti eläimeksi. Tämä onkin tärkeä ero. Riittiin osallistuva ihminen uskoi, että muutos on totta.
                Esityksen katsoja tietää, että näyttelijä esittää roolia. Teatteri alkaa siitä ettei nähtyjä tapahtumia
                pidetä oikeana todellisuuden tapahtumana.
            </p>
