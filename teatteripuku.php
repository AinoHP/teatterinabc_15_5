            <h2>Teatteripuku sopimuksen tuote?</h2>
            <p>Tavallisesta arkipukeutumisesta poikkeavat vaatteet ovat ilmeisesti olleet osa esittämistä sen
                alkuajoista lähtien. Kulloisenkin aikakauden peruspukeutumisesta poikkeava vaate on kautta aikojen
                auttaa esiintyjää eläytymään ja pääsemään helpommin sisälle siihen rooliin, jota hän on esittämässä. Kun
                esittämisellä ei ole enää uskonnollista yhteyttä, tavallisista vaatteista poikkeavat puvut ovat
                katsojalle merkki siitä, että se, mitä tapahtuu ei ole totta, vaan esitettyä.

                Sopimuksenvaraisuus ja merkinomaisuus ovat aina olleet osa esitysvaatteiden luonetta. Nämä sopimukset
                eivät tietenkään aina ole olleet tiedostettuja ja ne ovat vaihdelleet aikojen kuluessa. Esimerkki
                vahvasti sopimusluonteista puvuista ovat alun perin 1500-luvultaolevat Commedia dell´arte –tyylin
                roolihahmot ja heidän asunsa. Jokaisella heistä oli tunnistettava puku ja usein myös naamio, jotka
                nähtyään katsojat heti tiesivät, kuka roolihenkilö on ja minkälaisia ominaisuuksia hänellä on.
            </p>
                <p>Commedia dell´arte –tyylisuunta on peräisin Italiasta 1500-luvuta. Tyylilajin esittäjillä roolipuku,
                    luonne ja toimintatavat kuuluvat yhteen. Hahmot ovat tyyppihahmoja. Ne edustavat erilaisia
                    luonteita, ammattikuntia, yhteiskuntaluokkia, jonkun kaupungin tai maakunnan asukkaita.</p>
