            <h2>Taloudelliset tekijät</h2>
            <p>Yhteiskunnalliset tilanteet ovat aina vaikuttaneet esittäjiin, esityksiin ja niissä käytettyihin
                pukuihin. Eri aikoina esityspukujen materiaalit ovat valikoituneet sen mukaan, mitä on ollut saatavissa
                ja toisaalta kuinka paljon rahaa vaatteisiin on ollut käytössä. On tietenkin aivan eri asia, jos puvun
                maksaa renessanssiruhtinas tai rikas hallitsija rauhan aikana kuin jos esiintyjän pitää itse kustantaa
                esitysvaatteensa sellaisen aikana jolloin kaikesta materiaalista on pulaa. </p>
        </div>
        <br /><br /><br />
        <div class="column-100 inlineBlock">
            <h2>Puku kommentoi ympäröivää yhteiskuntaa</h2>
            <p>Esitykset ovat suhteessa ympäröivään yhteiskuntaan ja ne ovat suhteessa kaikkiin senhetkisiin
                tilanteisiin, tapahtumiin ja arvoihin. Ne voivat vahvistaa, tukea ja pitää vallitsevaa tilannetta hyvää
                ja hyväksyttävänä ja toisaalta ne voivat vastustaa olemassa olevia ajatustapoja, tyylejä ja haluavat
                tuulettaa ja muuttaa vanhoja asenteita. Puku on osa esitystä ja myös se heijastelee ja keskustelee
                nykyhetken ja sen tilanteiden kanssa.

                1990-luvulta lähtien monissa esityksissä Suomessa alettiin käyttää kirpputoreilta ostettuja vaatteita
                jotka muokattiin ja käsiteltiin esityksen tarpeita vastaaviksi. Tämä johtui monista eri syistä. Useat
                esitykset, joissa kirpputoripukuja käytettiin, olivat pienten vapaiden ryhmien tuottamia produktioita,
                joilla oli vain vähän rahaa käytössään ja kirpputoreilta sai halpoja vaatteita. Toisaalta vanhojen
                vaateiden uudelleen käyttämisen voi nähdä vaikkapa kannanottona liiallista kulutusta vastaan.</p>
