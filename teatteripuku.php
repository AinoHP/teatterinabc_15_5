            <h2>Mielikuva menneestä ajasta</h2>
            <p>Mielikuvat siitä, miltä jokin mennyt aikakausi näyttää ovat vahvoja, vaikka ne tietenkin muuttuvat
                aikojen kuluessa. Näiden mielikuvien syntyyn vaikuttavat tietomme historiasta ja se, miten asioita on
                aikaisemmin esitetty. Mielikuvat muuttuvat kun uutta tietoa julkaistaan, esitetään ja tuodaan yleiseen
