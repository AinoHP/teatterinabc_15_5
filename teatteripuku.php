            <h2>Mitkä asiat ja tekijät ovat vaikuttaneet siihen, miten esiintyjät ovat eri aikoina pukeutuneet tai
                puettu? </h2>
            <p>Esitysvaatteiden ulkonäkö ja tyyli ovat muuttuneet eri aikoina paljon. Esitysajankohdan pukeutumistyyli,
                kauneusihanteet, moraaliset arvot, taloudelliset tekijät, yhteiskunnalliset tilanteet, historian
                käsitykset ja esitystekniset tarpeet vaikuttavat puvuissa käytettyihin materiaaleihin, väreihin,
                malliin, leikkaukseen, hameen helman tai housujen lahkeen pituuteen…</p>
        </div>
        <br /><br /><br />
        <div class="column-100 inlineBlock">
            <h2>Muoti ja kauneusihanteet</h2>
            <p>Jokaisen ajankohdan käsitys kauneudesta ja muodista määrittää sitä, minkälaisia esityspuvut ovat olleet.
                Jos on haluttu, että esittäjä näyttää kauniilta, oman ajan tyyli on sanellut sen, mikä on kaunista ja
                mikä rumaa. Esitysajankohdan pukeutumistavat vaikuttavat myös siihen, miten asu valitaan, kun kuvataan
                tapahtumia muuna aikana. Kun barokkiteatterin aikaa esitettiin antiikin aikaa, miesnäyttelijällä saattoi
                olla yllään roomalaistyylinen haarniska 160-luvulta ja 1600-luvun peruukki. Tämänkaltainen yhdistelmä
                näyttäisi meidän näkökulmastamme hassulta, mutta omana aikanaan siinä ei olisi nähty mitään kummallista.

                Jollakin tavalla puvun tekohetken muoti näkyy aina. Voidaan sanoa, että missään esityksissä ei voi olla
                totuudenmukaista jonkin tietyn aikakauden historiallista pukua. Jokin osa vaatetta, leikkauksen
                yksityiskohta tai hihat paljastavat sen, milloin puku on tehty. Jokainen sukupolvi ajattelee toista
                aikaa esittävän puvun oman historiakäsityksensä ja oman muotinsa silmälasien kautta. </p>
        </div>
        <br /><br /><br />
        <div class="column-100 inlineBlock">
            <h2>Moraali ja sopivaisuuskäsitykset</h2>
            <p>Myös käsitykset siitä, mikä esittäjille ja esityksissä on sallittua ja ei ole sallittua vaihtuvat ja
                muuttuvat. On ollut aikakausia jolloin naisen nilkkakaan ei saanut vilahtaa ja jonain toisena aikana
                esittäjä saa olla alasti näyttämöllä. Rajoitukset ovat saattaneet liittyä myös sukupuoleen. Antiikin
                ajan näytelmissä esiintyivät vain miehet. Nuoret pojat esittivät naisten roolit: He esiintyivät naisten
                puvuissa ja käyttivät naamiota tai maskeerattiin naisen näköiseksi. Keskiajalla naiset saivat esiintyä
                ainoastaan mysteerinäytelmissä kirkoissa. Renessanssin aikana vain miehet saivat esiintyä julkisesti ja
                harjoittaa ammattimaista tanssia. Naiset saivat esiintyä ainoastaan yksityisissä tilaisuuksissa
                esimerkiksi hovin juhlissa.</p>
