                <h2>Epookkipukujen historiaa</h2>
                <p>Menneinä aikakausina näyttämön epookkipukuihin on suhtauduttu eri tavoin.
                  Keskiajalla ja pitkään sen jälkeen käytettiin esityksissä kunkin oman ajan pukuja vaikka kerrottiin esimerkiksi raamatun ajan tapahtumista.
                   1700-luvun lopulle saakka oli tapana, että samassa esityksessä osalla esiintyjistä saattoi olla vanhemman aikakauden tyyliset puvut ja osa esiintyi yllään oman ajan vaatetus.
                   1800-luvun kuluessa innostuttiin mahdollisimman ”aidoista” epookkipuvuista ja kopioitiin vanhojen maalausten pukuja näyttämölle.
                    1900-luvun alussa pukujen tyylittely, materiaalien käsittely esimerkiksi värjäämällä, maalaamalla ja applikoimalla  yleistyi.
                    Pukujen avulla tavoiteltiin ulkoisen realismin sijasta sisäistä tunnetta tai kokonaistunnelmaa.
                    Tyylittelyn ja realismin vaihtelu epookkipuvuissa jatkuu edelleen.
                     Epookkipuvut kertovat siis myös esityksen valmistusajan taiteellisista arvoista ja kauneusihanteista.</p>
