                            <h2>Värjääminen</h2>
                            <p>Kangasliikkeistä hankittavat kankaat eivät aina ole halutun värisiä ja välillä tarvittavan suuria määriä on mahdotonta löytää valmiina.
                               Kankaita värjäämällä voidaan vaikuttaa yksittäisen puvun luomaan tunnelmaan ja toisaalta luoda halutun värisiä ryhmiä näyttämölle.
                                Tällöin saatetaan hankkia suuria määriä samaa vaaleata hyvin värjäytyvää kangasta, ja värjätä erivärisiä kokonaisuuksia.
                                 Suurimmissa teatteritaloissa on usein hyvin varustellut värjäämöt, erilliset värjäämiseen tarkoitetut pesukoneet ja erityiset tilat patinoimiselle,
                                 jotta samalla ei vahingossa sotketa muita pukuja.
                                  Vähävaraisemman teatteriryhmän pukusuunnittelija puolestaan saattaa värjätä omassa kylpyhuoneessaan. </p>

