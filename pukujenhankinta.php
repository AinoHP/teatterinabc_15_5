                          <h1>Uusien materiaalien ja teknologian käyttö näyttämöpuvuissa.</h1>
                            <h2>Luonnonmateriaaleista keinokuitukankaisiin ja vaahtomuoviin</h2>
                            <p>Kuten kaikki muutkin vaatteet, myös esiintymispuvut valmistettiin pitkään luonnonmateriaaleista valmistetuista kankaista, kuten puuvillasta, silkistä ja villasta.
                               Vähitellen 1900-luvun alkupuolelta lähtien erilaiset keinokuiduista valmistetut kankaat alkoivat saavuttaa suosiota.
                              Varsinainen tekokuitukankaiden läpimurto tapahtui 1930-40-luvun vaihteessa.
                             1950- ja 1960-luvuilla vaatteiden valmistuksessa käytettiin runsaasti tekokuitukankaita ja myös erilaisten joustomateriaalien määrä alkoi lisääntyä. '
                             Helposti muovattavaa ja veistettävää vaahtomuovia alettiin käyttää teatterissa ja oopperassa niin toppauksissa, pukujen koristelussa, kuin päähineissä ja naamioissa.
                             Etenkin 1980-luvulla kauppoihin saatiin runsaasti uusia teknisiä, joustavia materiaaleja. Nykyisin tarjolla olevien materiaalien kirjo on valtava.  </p>
