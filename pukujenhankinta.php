                                            <h2>Päälle puettava elektroniikka ja muut keksinnöt</h2>
                                            <p> 2000-luvun mittaan puettava elektroniikka on tullut tunnetuksi etenkin urheiluun ja terveydenhoitoon liittyvässä vaatetuksessa, mutta etenkin tanssipukuihin on yhdistetty uusimpia teknologisia keksintöjä jo 1800-luvulta saakka.
                                               Tanssijoiden päähineisiin saatettiin kiinnittää sähkövaloja, joiden patterit kätkettiin pukujen sisälle,  pukuja valaistiin värillisillä valoilla ja niihin heijastettiin erilaisia kuvia.
                                                1900-luvulla tanssipuvut saatiin hohtamaan pimeässä myös maalaamalla ne itsevalaisevalla, radiumia sisältävällä maalilla, ennen kuin ymmärrettiin radiumin vaarallisuus.
                                               Myöhemmin on kehitetty erilaisia turvallisempia pimeässä hohtavia maaleja, joita on käytetty esiintymispukuihin sekä maskeeraukseen.
                                                2000-luvun alusta lähtien esiintymispuvuissa on käytetty led-valoja, jotka saadaan reagoimaan esimerkiksi sydämen sykkeeseen tai kehon lämpötilaan.
                                                 Pukuihin on mahdollista ommella joustavaa elektroniikkaa tai vaikkapa tanssijoiden liikkeisiin reagoivia, ääntä ja musiikkia tuottavia sensoreita. </p>

