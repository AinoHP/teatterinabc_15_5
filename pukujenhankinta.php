                    <h2>Pukujen patinointi</h2>
                    <p>1970-luvulla ja 1980-luvulla saatiin uutta tietoa myös siitä, miten materiaaleihin luotiin käytön jälkiä.
                       Jo 1960-luvulta lähtien elokuvissa tutuksi tullut aiempaa realistisempi, dokumentaarinen tyyli vaikutti myös näyttämöllä,
                        sillä pukuja alettiin yhä useammin patinoida eli mekaanisesti kuluttaa ja käsitellä esimerkiksi vanhan, likaisen tai märän näköiseksi.
                      Materiaaleja saatettiin myös muokata toisen näköiseksi, esimerkiksi paperista tehtiin nahan tai metallin näköistä</p>

                      <p>Koska näyttämön voimakkaissa valoissa patinointi helposti katoaa näkyvistä, pukuja on pitänyt käsitellä yhtä lailla voimakkaasti.
                         Läheltä katsottuna puvut voivatkin näyttää erikoisilta, mutta pitää muistaa, että ne on tarkoitettu katsottavaksi matkan päästä. </p>
