          <h2>Pukujen kierrätystä</h2>
        <p>Kautta aikojen pukuja on hankittu käytettyinä yksityisiltä henkilöiltä sekä käytettyjen vaatteiden kauppiailta.
          Pukuja on myös lainattu ja vuokrattu toisilta teattereilta pientä korvausta vastaan.
           Varastossa olleita pukuja on kierrätetty uusissa esityksissä etenkin avustajien ja kuoron yllä silloinkin,
           kun pääosanesittäjät tai solistit saivat uudet puvut.
Sota- ja pula-aikoina kierrättäminen on ollut välttämätöntä, sillä materiaaleja ei ole ollut tarjolla.
Tällöin kaikki jo varastoissa ollut materiaali käytettiin hyväksi, korjattiin ja kierrätettiin huolellisesti.
Etenkin 1970-luvulta lähtien Suomessa on perustettu useita pieniä teatteri- ja tanssiryhmiä sekä myöhemmin myös oopperaryhmiä,
 joilla ei välttämättä ole varsinaista omaa puvustoa, eli henkilökuntaa ja paikkaa,
  jossa pukuja ommellaan, korjataan, huolletaan ja säilytetään, mutta ei myöskään suuria rahavaroja.
   Usein puvut hankitaankin kirpputoreilta ja kierrätyskeskuksista.
   Monille pukusuunnittelijoille materiaalien ja vaatteiden kierrättäminen on myös tärkeä ekologinen valinta.
    Käytetyt ja kuluneet materiaalit, joilla on jo tarina, puhuttelevat eri tavalla kuin uutuudenkiiltävät kankaat.  </p>
