        <h2>Pukuhankintojen historiaa</h2>
        <p>Puvut ovat monin tavoin tärkeä osa esitystä, mutta niiden suunnittelu, toteutus ja hankkiminen myös maksavat.
          Teatteri- ja oopperaseurueilla tai esiintyjillä itsellään ei useinkaan ole ollut käytössään suuria rahasummia,
          joten pukuja on pyritty hankkimaan edullisesti.
           Pukujen hankinnasta on tietoja keskiajalta lähtien. Euroopassa hallitsijat ja aateliset saattoivat lahjoittaa vanhoja pukujaan suosimilleen teatteriseurueille,
            tai lainata pukuja kuninkaallisesta vaatevarastosta hoviteatterissa esiintyneille seurueille.
             1600-luvulta lähtien Ranskassa ja Englannissa tiedetään perustetun pukuvuokraamoja,
             jotka helpottivat esiintymispukujen hankkimista uusiin näytelmiin etenkin silloin kun esiintyjät itse olivat vastuussa pukujen kustannuksista.
           1800-luvun lopulla Saksassa perustettiin  yhtiöitä, jotka erikoistuivat esiintymispukujen ja niihin liittyvät tarpeiston ja valmistustarvikkeiden myyntiin.
            Näitä pukuja ostettiin myös Suomeen eri teattereihin ja oopperaa varten.   </p>

